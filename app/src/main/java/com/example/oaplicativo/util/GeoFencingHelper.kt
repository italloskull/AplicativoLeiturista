            }
        }
        return null
    }

    /**
     * Identifica a rota (sub-grupo) baseado na proximidade da linha (LineString).
     * Ideal para desenhos de ruas. Usa um raio de tolerância (ex: 35 metros).
     */
    fun findSuggestedRoute(cidadeNome: String?, lat: Double?, lng: Double?): String? {
        if (lat == null || lng == null || cidadeNome?.uppercase() != TARGET_CITY) return null

        val p = Point(lat, lng)

        var bestRoute: String? = null
        var minDistance = Double.MAX_VALUE

        for ((routeName, path) in ITAPOA_ROUTES) {
            // INDEXAÇÃO ESPACIAL: Verifica o Bounding Box antes do cálculo pesado
            val bbox = ROUTE_BOUNDING_BOXES[routeName] ?: continue
            if (!isPointInBoundingBox(p, bbox)) continue

            val dist = minDistanceToPath(p, path)
            if (dist < ROUTE_DETECTION_TOLERANCE_METERS && dist < minDistance) {
                minDistance = dist
                bestRoute = routeName
            }
        }
        return bestRoute
    }

    /**
     * Calcula a menor distância em metros de um ponto até uma linha (caminho de pontos).
     */
    private fun minDistanceToPath(point: Point, path: List<Point>): Double {
        var minDest = Double.MAX_VALUE
        for (i in 0 until path.size - 1) {
            val d = distanceToSegment(point, path[i], path[i + 1])
            if (d < minDest) minDest = d
        }
        return minDest
    }

    /**
     * Algoritmo de distância entre ponto e segmento de reta com correção de Haversine (metros).
     */
    private fun distanceToSegment(p: Point, a: Point, b: Point): Double {
        val latP = p.lat * PI / 180.0
        val lngP = p.lng * PI / 180.0
        val latA = a.lat * PI / 180.0
        val lngA = a.lng * PI / 180.0
        val latB = b.lat * PI / 180.0
        val lngB = b.lng * PI / 180.0

        val x = (lngB - lngA) * cos((latA + latB) / 2)
        val y = latB - latA
        val lenSq = x * x + y * y
        
        if (lenSq == 0.0) return haversine(p.lat, p.lng, a.lat, a.lng)

        val t = ((lngP - lngA) * cos((latA + latB) / 2) * x + (latP - latA) * y) / lenSq
        val constrainedT = max(0.0, min(1.0, t))

        val projLat = a.lat + constrainedT * (b.lat - a.lat)
        val projLng = a.lng + constrainedT * (b.lng - a.lng)

        return haversine(p.lat, p.lng, projLat, projLng)
    }

    private fun haversine(lat1: Double, lon1: Double, lat2: Double, lon2: Double): Double {
        val dLat = (lat2 - lat1) * PI / 180.0
        val dLon = (lon2 - lon1) * PI / 180.0
        val a = sin(dLat / 2) * sin(dLat / 2) +
                cos(lat1 * PI / 180.0) * cos(lat2 * PI / 180.0) *
                sin(dLon / 2) * sin(dLon / 2)
        val c = 2 * atan2(sqrt(a), sqrt(1 - a))
        return EARTH_RADIUS_METERS * c
    }

    /**
     * Indexação Espacial: Calcula o Bounding Box de uma lista de pontos.
     */
    private fun calculateBoundingBox(points: List<Point>): BoundingBox {
        var minLat = Double.MAX_VALUE
        var maxLat = -Double.MAX_VALUE
        var minLng = Double.MAX_VALUE
        var maxLng = -Double.MAX_VALUE

        for (p in points) {
            if (p.lat < minLat) minLat = p.lat
            if (p.lat > maxLat) maxLat = p.lat
            if (p.lng < minLng) minLng = p.lng
            if (p.lng > maxLng) maxLng = p.lng
        }
        
        // Adiciona uma pequena margem ao Bounding Box para cobrir a tolerância
        val margin = 0.001 // ~100 metros
        return BoundingBox(minLat - margin, maxLat + margin, minLng - margin, maxLng + margin)
    }

    private fun isPointInBoundingBox(p: Point, bbox: BoundingBox): Boolean {
        return p.lat in bbox.minLat..bbox.maxLat && p.lng in bbox.minLng..bbox.maxLng
    }

    /**
     * Algoritmo de Ray Casting (Ponto em Polígono) - Mantido apenas para Grupos Legados
     */
    private fun isPointInPolygon(point: Point, polygon: List<Point>): Boolean {
        var isInside = false
        var j = polygon.size - 1
        for (i in polygon.indices) {
            if (polygon[i].lng > point.lng != polygon[j].lng > point.lng &&
                point.lat < (polygon[j].lat - polygon[i].lat) * (point.lng - polygon[i].lng) / (polygon[j].lng - polygon[i].lng) + polygon[i].lat
            ) {
                isInside = !isInside
            }
            j = i
        }
        return isInside
    }
