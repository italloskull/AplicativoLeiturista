            val groupNumber = route.split(".").firstOrNull()
            if (groupNumber != null) return "GRUPO $groupNumber"
        }

        // Se não achou na rota (áreas novas ainda não mapeadas), tenta pelo polígono de Grupo legado
        val p = Point(lat, lng)
