    fun findSuggestedGroup(cidadeNome: String?, lat: Double?, lng: Double?): String? {
        if (lat == null || lng == null || cidadeNome?.uppercase() != TARGET_CITY) return null

        // Primeiro tentamos achar a Rota (mais granular)
        val route = findSuggestedRoute(cidadeNome, lat, lng)
