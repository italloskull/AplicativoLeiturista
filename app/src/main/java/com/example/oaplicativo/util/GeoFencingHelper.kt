    /**
     * Identifica o grupo baseado na coordenada e cidade.
     */
    fun findSuggestedGroup(cidadeNome: String?, lat: Double?, lng: Double?): String? {
        if (lat == null || lng == null || cidadeNome?.uppercase() != "ITAPOÁ") return null

        val p = Point(lat, lng)

        for ((groupName, polygon) in ITAPOA_GROUPS) {
            if (isPointInPolygon(p, polygon)) {
                return groupName
            }
        }
        return null
    }

    /**
     * Algoritmo de Ray Casting (Ponto em Polígono)
     */
    private fun isPointInPolygon(point: Point, polygon: List<Point>): Boolean {
        var isInside = false
        var j = polygon.size - 1
        for (i in polygon.indices) {
            if (polygon[i].lng > point.lng != polygon[j].lng > point.lng &&
                point.lat < (polygon[j].lat - polygon[i].lat) * (point.lng - polygon[i].lng) / (polygon[j].lng - polygon[i].lng) + polygon[i].lat
            ) {
                isInside = !isInside
            }
            j = i
        }
        return isInside
    }
